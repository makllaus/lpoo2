    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (jTextFieldUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o usuário para continuar!");
            jTextFieldUser.requestFocus();
        } else if (jPasswordFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha a senha para continuar!");
            jPasswordFieldSenha.requestFocus();
        } else if (jPasswordFieldConfirmSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Confirme a senha para continuar!");
            jPasswordFieldConfirmSenha.requestFocus();
        } else {
            if (flag == 1) {
                modUser.setUsername(jTextFieldUser.getText());
                modUser.setTipo((String) jComboBoxTipo.getSelectedItem());
                modUser.setPassword(jPasswordFieldSenha.getText());
                ctrlUser.salvar(modUser);
                jPasswordFieldSenha.setText("");
                jPasswordFieldConfirmSenha.setText("");
                jTextFieldUser.setText("");
                jButtonSave.setEnabled(false);
                jTextFieldUser.setEnabled(false);
                jPasswordFieldSenha.setEnabled(false);
                jPasswordFieldConfirmSenha.setEnabled(false);
                jComboBoxTipo.setEnabled(false);
                jButtonCancel.setEnabled(false);
                jButtonNew.setEnabled(true);
                jTextFieldPesquisa.setEnabled(true);
                jButtonPesquisar.setEnabled(true);
            } else {
                modUser.setCod(Integer.parseInt(jTextFieldID.getText()));
                modUser.setUsername(jTextFieldUser.getText());
                modUser.setTipo(String.valueOf(jComboBoxTipo.getSelectedItem()));
                modUser.setPassword(jPasswordFieldSenha.getText());
                ctrlUser.editar(modUser);
                jPasswordFieldSenha.setText("");
                jPasswordFieldConfirmSenha.setText("");
                jTextFieldUser.setText("");
                jTextFieldPesquisa.setText("");
                jTextFieldID.setText("");
                jButtonSave.setEnabled(false);
                jTextFieldUser.setEnabled(false);
                jPasswordFieldSenha.setEnabled(false);
                jPasswordFieldConfirmSenha.setEnabled(false);
                jComboBoxTipo.setEnabled(false);
                jButtonEdit.setEnabled(false);
                jButtonNew.setEnabled(true);
                jButtonCancel.setEnabled(false);
                jTextFieldPesquisa.setEnabled(true);
                jButtonPesquisar.setEnabled(true);
            }
        }
        preencherTabela("select * from usuarios order by user_name");
    }//GEN-LAST:event_jButtonSaveActionPerformed

