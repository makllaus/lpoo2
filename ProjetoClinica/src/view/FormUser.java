    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        if (jTextFieldPesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo pesquisa para continuar!");
            jTextFieldPesquisa.requestFocus();
        } else {
            modUser.setPesquisa(jTextFieldPesquisa.getText());
            BeansUser model = ctrlUser.buscaUser(modUser);
            if (model.getCod() == 0) {
                jPasswordFieldConfirmSenha.setText("");
                jPasswordFieldSenha.setText("");
                jTextFieldUser.setText("");
                jTextFieldPesquisa.setText("");
                jTextFieldID.setText("");
            } else {
                jTextFieldID.setText(String.valueOf(model.getCod()));
                jTextFieldUser.setText(model.getUsername());
                jComboBoxTipo.setSelectedItem(model.getTipo());
                jButtonEdit.setEnabled(true);
                jButtonDelete.setEnabled(true);
                preencherTabela("select * from usuarios where user_name like'%"+model.getPesquisa()+"%'");
            }
        }

    }//GEN-LAST:event_jButtonPesquisarActionPerformed

