    private void jTableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUserMouseClicked
        String nome = "" + jTableUser.getValueAt(jTableUser.getSelectedRow(), 1);
        conn.connect();
        conn.executeSql("select * from usuarios where user_name='" + nome + "'");
        try {
            conn.rs.first();
            jTextFieldID.setText(String.valueOf(conn.rs.getInt("cod")));
            jTextFieldUser.setText(conn.rs.getString("user_name"));
            jPasswordFieldSenha.setText(conn.rs.getString("pass"));
            jComboBoxTipo.setSelectedItem(conn.rs.getString("tipo"));
            jButtonEdit.setEnabled(true);
            jButtonDelete.setEnabled(true);
        } catch (SQLException ex) {
            System.out.println("Erro ao selecionar linha \nerro: " + ex.getMessage());
        }
        conn.desconnect();
        jButtonNew.setEnabled(false);
        jButtonCancel.setEnabled(true);
    }//GEN-LAST:event_jTableUserMouseClicked
    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Username", "Senha", "Tipo"};
        conn.connect();
        conn.executeSql(sql);
        try {
            conn.rs.first();
            do {
                dados.add(new Object[]{conn.rs.getInt("cod"), conn.rs.getString("user_name"), conn.rs.getString("pass"), conn.rs.getString("tipo")});

            } while (conn.rs.next());

        } catch (SQLException ex) {
            System.out.println("Erro ao preecher arrayList \nerro: " + ex.getMessage());
        }

        BeansModelTable modTable = new BeansModelTable(dados, colunas);

        jTableUser.setModel(modTable);
        jTableUser.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableUser.getColumnModel().getColumn(0).setResizable(false);
        jTableUser.getColumnModel().getColumn(1).setPreferredWidth(273);
        jTableUser.getColumnModel().getColumn(1).setResizable(false);
        jTableUser.getColumnModel().getColumn(2).setPreferredWidth(207);
        jTableUser.getColumnModel().getColumn(2).setResizable(false);
        jTableUser.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableUser.getColumnModel().getColumn(3).setResizable(false);
        jTableUser.getTableHeader().setReorderingAllowed(false);
        jTableUser.setAutoResizeMode(jTableUser.AUTO_RESIZE_OFF);
        jTableUser.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conn.desconnect();
    }

