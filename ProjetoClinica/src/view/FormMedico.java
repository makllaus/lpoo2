    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o nome para continuar!");
            jTextFieldNome.requestFocus();
        } else if (jTextFieldCRM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o CRM para continuar!");
            jTextFieldCRM.requestFocus();
        } else {
            if (flag == 1) {
                modMed.setNome(jTextFieldNome.getText());
                modMed.setEspecialidade((String) jComboBoxEspecialista.getSelectedItem());
                modMed.setCrm(Integer.parseInt(jTextFieldCRM.getText()));
                ctrlMed.salvar(modMed);
                jTextFieldCRM.setText("");
                jTextFieldNome.setText("");
                jTextFieldID.setText("");
                jButtonSave.setEnabled(false);
                jTextFieldNome.setEnabled(false);
                jTextFieldCRM.setEnabled(false);
                jComboBoxEspecialista.setEnabled(false);
                jButtonCancel.setEnabled(false);
                jButtonNew.setEnabled(true);
                jTextFieldPesquisa.setEnabled(true);
                jButtonPesquisar.setEnabled(true);
            } else {
                modMed.setCod(Integer.parseInt(jTextFieldID.getText()));
                modMed.setNome(jTextFieldNome.getText());
                modMed.setEspecialidade(String.valueOf(jComboBoxEspecialista.getSelectedItem()));
                modMed.setCrm(Integer.parseInt(jTextFieldCRM.getText()));
                ctrlMed.editar(modMed);
                jTextFieldCRM.setText("");
                jTextFieldNome.setText("");
                jTextFieldPesquisa.setText("");
                jTextFieldID.setText("");
                jButtonSave.setEnabled(false);
                jTextFieldNome.setEnabled(false);
                jTextFieldCRM.setEnabled(false);
                jComboBoxEspecialista.setEnabled(false);
                jButtonEdit.setEnabled(false);
                jButtonNew.setEnabled(true);
                jButtonCancel.setEnabled(false);
                jTextFieldPesquisa.setEnabled(true);
                jButtonPesquisar.setEnabled(true);
            }
        }
        preencherTabela("select * from medico order by nome");
    }//GEN-LAST:event_jButtonSaveActionPerformed

