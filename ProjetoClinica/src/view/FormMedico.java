    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (!jTextFieldID.getText().isEmpty()) {
            int res = 0;
            res = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir?");
            if (res == JOptionPane.YES_OPTION) {
                modMed.setCod(Integer.parseInt(jTextFieldID.getText()));
                ctrlMed.excluir(modMed);
                jTextFieldCRM.setText("");
                jTextFieldNome.setText("");
                jTextFieldPesquisa.setText("");
                jTextFieldID.setText("");
                jButtonSave.setEnabled(false);
                jTextFieldNome.setEnabled(false);
                jTextFieldCRM.setEnabled(false);
                jComboBoxEspecialista.setEnabled(false);
                jButtonEdit.setEnabled(false);
                jButtonNew.setEnabled(true);
                jButtonCancel.setEnabled(false);
                jButtonDelete.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um médico para excluir!");
            jTextFieldPesquisa.requestFocus();
        }
        preencherTabela("select * from medico order by nome");
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jTableMedicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMedicoMouseClicked
        String nome = "" + jTableMedico.getValueAt(jTableMedico.getSelectedRow(), 1);
        conn.connect();
        conn.executeSql("select * from medico where nome='" + nome + "'");
        try {
            conn.rs.first();
            jTextFieldID.setText(String.valueOf(conn.rs.getInt("cod")));
            jTextFieldNome.setText(conn.rs.getString("nome"));
            jTextFieldCRM.setText(String.valueOf(conn.rs.getInt("crm")));
            jComboBoxEspecialista.setSelectedItem(conn.rs.getString("especialidade"));
            jButtonEdit.setEnabled(true);
            jButtonDelete.setEnabled(true);
        } catch (SQLException ex) {
            System.out.println("Erro ao selecionar linha \nerro: " + ex.getMessage());
        }
        conn.desconnect();
        jButtonNew.setEnabled(false);
        jButtonCancel.setEnabled(true);
    }//GEN-LAST:event_jTableMedicoMouseClicked

    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Nome", "Especialidade", "CRM"};
        conn.connect();
        conn.executeSql(sql);
        try {
            conn.rs.first();
            do {
                dados.add(new Object[]{conn.rs.getInt("cod"), conn.rs.getString("nome"), conn.rs.getString("especialidade"), conn.rs.getInt("crm")});

            } while (conn.rs.next());

        } catch (SQLException ex) {
            System.out.println("Erro ao preecher arrayList \nerro: " + ex.getMessage());
        }

        BeansModelTable modTable = new BeansModelTable(dados, colunas);

        jTableMedico.setModel(modTable);
        jTableMedico.getColumnModel().getColumn(0).setPreferredWidth(33);
        jTableMedico.getColumnModel().getColumn(0).setResizable(false);
        jTableMedico.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableMedico.getColumnModel().getColumn(1).setResizable(false);
        jTableMedico.getColumnModel().getColumn(2).setPreferredWidth(107);
        jTableMedico.getColumnModel().getColumn(2).setResizable(false);
        jTableMedico.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableMedico.getColumnModel().getColumn(3).setResizable(false);
        jTableMedico.getTableHeader().setReorderingAllowed(false);
        jTableMedico.setAutoResizeMode(jTableMedico.AUTO_RESIZE_OFF);
        jTableMedico.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conn.desconnect();
    }

