    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        if (!jTextFieldPesquisa.getText().isEmpty()) {
            modMed.setPesquisa(jTextFieldPesquisa.getText());
            BeansMedico model = ctrlMed.buscaMedico(modMed);
            if (model.getCod() == 0) {
                jTextFieldCRM.setText("");
                jTextFieldNome.setText("");
                jTextFieldPesquisa.setText("");
                jTextFieldID.setText("");
            } else {

                jTextFieldID.setText(String.valueOf(model.getCod()));
                jTextFieldCRM.setText(String.valueOf(model.getCrm()));
                jTextFieldNome.setText(model.getNome());
                jComboBoxEspecialista.setSelectedItem(model.getEspecialidade());
                jButtonEdit.setEnabled(true);
                jButtonDelete.setEnabled(true);
                preencherTabela("select * from medico where nome like'%" + model.getPesquisa() + "%'");
            }
        } else {
            jButtonEdit.setEnabled(false);
            jButtonNew.setEnabled(true);
            jButtonCancel.setEnabled(false);
            jButtonDelete.setEnabled(false);
            jTextFieldCRM.setText("");
            jTextFieldNome.setText("");
            jTextFieldPesquisa.setText("");
            jTextFieldID.setText("");
            preencherTabela("select * from medico order by nome");
        }


    }//GEN-LAST:event_jButtonPesquisarActionPerformed

