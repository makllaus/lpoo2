    public void preencherBairros(){
        conn.connect();
        conn.executeSql("select * from medico order by nome");
        
        try {
            conn.rs.first();
            jComboBoxMedico.removeAllItems();
            do {
                jComboBoxMedico.addItem(conn.rs.getString("nome"));
            } while (conn.rs.next());
            
        } catch (SQLException ex) {
            Logger.getLogger(FormPaciente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Erro ao buscar bairros no banco de dados\n contate o adm do sistema");
        }
       
        
        
        conn.desconnect();
    }
